import os
import yaml
import pandas as pd
from openai import OpenAI
from pydantic import BaseModel, Field
from dotenv import load_dotenv

load_dotenv()

with open("config.yml", "r") as file:
    config = yaml.safe_load(file)

generation_provider = config["pipeline"]["provider"].lower()
judge_provider = config["evaluation"]["judge_provider"].lower()
model_to_use = config["evaluation"]["judge_model"]
generation_temp = config["evaluation"]["judge_temperature"]

print(f"Loaded configuration: Using {judge_provider.upper()} with model {model_to_use}")

generation_input_column = f"{generation_provider}_summary"
input_filename = f"data/generation_outputs/{generation_provider}_generation_results.csv"
output_filename = f"data/evaluation_outputs/{judge_provider}_evaluated_results.csv"

if judge_provider == "lmstudio":
    client = OpenAI(
        api_key=os.getenv("LMSTUDIO_API_KEY", "lmstudio"),
        base_url="http://127.0.0.1:1234/v1",
    )
elif judge_provider == "openai":
    api_key = os.getenv("OPENAI_API_KEY")
    if not api_key:
        raise ValueError("OPENAI_API_KEY not found! Please check your .env file.")
    client = OpenAI(api_key=api_key)
else:
    raise ValueError(f"Judge provider '{judge_provider}' is not supported by evaluation_pipeline.py.")

class GradingReport(BaseModel):
    biological_synthesis_score: int = Field(
        description="Score from 1 to 5 based on rubric. Requires mapping back to molecular pathways without hallucinations."
    )
    biological_synthesis_justification: str = Field(
        description="Brief justification for the assigned biological synthesis score."
    )
    systematic_reasoning_score: int = Field(
        description="Score from 1 to 5 based on rubric. Evaluates if the model diagnosed the true transcriptomic root cause."
    )
    systematic_reasoning_justification: str = Field(
        description="Brief justification for the assigned systematic reasoning score."
    )
    clinical_actionability_score: int = Field(
        description="Score from 1 to 5 based on rubric. Evaluates utility for decision making and firm grounding in literature."
    )
    clinical_actionability_justification: str = Field(
        description="Brief justification for the assigned clinical actionability score."
    )


grading_report_schema = {
    "name": "grading_report",
    "schema": GradingReport.model_json_schema(),
    "strict": True,
}

evaluator_instruction = """
You are an expert, independent academic auditor specializing in bioinformatics and genomic oncology.
Your job is to strictly evaluate clinical text generated by a Clinical Decision Support System (CDSS) against a 5-point grading matrix.

You must evaluate three specific domains:
1. Biological Synthesis: (1=Simple restatement, 3=Broad biological differences, 5=Explicit, hallucination-free pathway mapping).
2. Systematic Reasoning: (1=No logical root cause, 3=Generic oncological reasoning, 5=Diagnoses exact root cause via unique transcriptomic footprint).
3. Clinical Actionability: (1=Raw data translation, 3=Clinically coherent but lacks external grounding, 5=Clear, literature-backed recommendation).

Be highly critical. Only award a 4 or 5 if the text rigorously fulfills the 'Exceptional' criteria outlined in the rubric.
"""

if not os.path.exists(input_filename):
    raise FileNotFoundError(f"Could not find {input_filename}. Please run the generation script first!")

df = pd.read_csv(input_filename)
evaluation_results = []

print("Initiating LLM-as-a-Judge Evaluation Pipeline...\n")

for index, row in df.head(5).iterrows():
    patient_id = row['patient_id']
    generated_text = row.get(generation_input_column)
    
    print(f"Auditing Report for Patient: {patient_id}...")
    
    if pd.isna(generated_text) or "Error" in generated_text:
        print(f"Skipping {patient_id} due to missing input text.")
        continue

    prompt = f"""
    Please evaluate and score the following generated clinical summary:
    
    --- START OF CDSS OUTPUT ---
    {generated_text}
    --- END OF CDSS OUTPUT ---
    
    Return the final scores and justifications strictly matching the requested JSON schema.
    """

    try:
        response = client.chat.completions.create(
            model=model_to_use,
            temperature=generation_temp,
            messages=[
                {"role": "system", "content": evaluator_instruction},
                {"role": "user", "content": prompt},
            ],
            response_format={"type": "json_schema", "json_schema": grading_report_schema},
        )

        parsed_report = GradingReport.model_validate_json(response.choices[0].message.content)
        
        evaluation_results.append({
            'patient_id': patient_id,
            generation_input_column: generated_text,
            'bio_synthesis_score': parsed_report.biological_synthesis_score,
            'bio_synthesis_justification': parsed_report.biological_synthesis_justification,
            'sys_reasoning_score': parsed_report.systematic_reasoning_score,
            'sys_reasoning_justification': parsed_report.systematic_reasoning_justification,
            'clinical_action_score': parsed_report.clinical_actionability_score,
            'clinical_action_justification': parsed_report.clinical_actionability_justification
        })
        print(f"--- SUCCESS: Successfully audited {patient_id} ---")
        
    except Exception as e:
        print(f"Error evaluating patient {patient_id}: {e}")

final_df = pd.DataFrame(evaluation_results)
final_df.to_csv(output_filename, index=False)

print(f"\nEvaluation pipeline complete. Matrix scores stored successfully in: {output_filename}")

if not final_df.empty:
    print("\n--- Current Performance Metrics ---")
    print(f"Mean Biological Synthesis: {final_df['bio_synthesis_score'].mean():.2f}")
    print(f"Mean Systematic Reasoning: {final_df['sys_reasoning_score'].mean():.2f}")
    print(f"Mean Clinical Actionability: {final_df['clinical_action_score'].mean():.2f}")